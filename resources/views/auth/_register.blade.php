<script>

    function register(){
        submitLoading();
        $.ajax({
            url: "{{route('register.store')}}",
            type: 'POST',
            data: {
                "name": $('#name').val(),
                "email": $('#email').val(),
                "password": $('#password').val(),
                "terms": $('#checkbox').is(":checked"),
                "_token": "{{ csrf_token() }}",
            },
            success: function (data) {
                Swal.fire('Success', data.message, 'success').then(e => {
                    submitReset();
                    window.location.href = "{{route('login', ['type' => 'expert'])}}";
                });
            },
            error: function (data) {
                Swal.fire('Error!', data.responseJSON.message, 'error').then(() => submitReset() );
            }
        });
    }

    function quick_view(id){
        $.ajax({
            url: "{{route('post.quick_view')}}",
            type: 'GET',
            data: {
                "id": id,
                "_token": "{{ csrf_token() }}",
            },
            success: function (data) {
                $('#modalLarge').modal('show')
                $('#modalLarge .modal-body').html(data);
            }
        });
    }
